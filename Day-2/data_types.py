# Primitive Data types
# Strings, Integers, FLoats, Boolean

# Subscripting
name = "Neeraj"
print(name[0])
print(name[-1])
print(name[2])
print(name[4])


# String
s = "123"+"345" # Concatenation
print(s)

# Integer = Whole Number
i = 123+456
print(i)

#Large Number

l = 123_456_789 #we can write number by providing underscore , interpreter will ignore the underscore during the runtime
print(l)


#Float = floating point number
print(3.148)

#Booleans

print(True) # this True False will be the first letter in capital
print(False)


